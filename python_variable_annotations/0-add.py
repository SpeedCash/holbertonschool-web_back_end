#!/usr/bin/env python3
"""
Module to add two float numbers and return the result.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers.

    Args:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of a and b.
    """
    return a + b
